# What is a Variable?
#A variable is simply a container that stores a value.

age = 25
name = "Harry"

# age is the variable, 25 is the value stored inside it


# What are Data Types?
# A data type tells Python what kind of value a variable is holding.


# Integer (int) , Integers are whole numbers without decimals.

students = 100
orders = 45


# Float (decimal numbers) Floats are numbers that contain decimals.

price = 2999.99
rating = 4.5


# String (text data) Text values in Python are called strings. Anything written inside single or double quotes is a string.

city = "Delhi"
email = "harry@gmail.com"

# Boolean (True or False) Boolean values represent logical conditions.

is_active = True
is_paid = False


# How Python Identifies Data Types
# Python automatically detects the data type of a value. You do not need to specify it manually.

# You can check the data type using the type() function:

print(type(age))
type(name)